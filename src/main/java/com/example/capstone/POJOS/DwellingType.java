package com.example.capstone.POJOS;

/**
 *  The different dwelling types a HomeOwner can live in
 */
public enum DwellingType {

    /**
     *  Freestanding residential building
     */
    SINGLE,

    /**
     *  Either a top or bottom apartment in a house
     */
    APARTMENT,

    /**
     *  Smaller house, often with one floor
     */
    BUNGALOW,

    /**
     *  Home connected to another with a single wall
     */
    SEMI_ATTACHED
}
